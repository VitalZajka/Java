package Variables;

public class ExerciseVariables {

	public static void main(String[] args) {
		//Types of Variables
		//Primitive
		//--Number family
		byte b=-128;
		short s=31987;
		long card=1234567890123456l;
		int i=4567;
		//Decimals
		float f=1.81f;
		double d=890.8654;
		//Cgaracter
		char c='&';
		//Boolean
		boolean g=true;
		//Non Primitive--"String in Java is a class of its own" (?!)
		String str="Ahjf&5kjnc096#";
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(card);
		System.out.println(i);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(g);
		System.out.println(str);
		
		
	
		

	}

}
